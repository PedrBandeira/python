# Este código demonstra como imprimir a mensagem "Olá, Mundo!" diretamente usando o comando print e também como atribuir a mensagem a uma variável e imprimi-la.

# Imprime a mensagem "Olá, Mundo!" diretamente usando o print:
print('Olá, Mundo!')

# Atribui a mensagem a uma variável e imprime usando print:
mensagem = 'Olá novamente, Mundo!'
print(mensagem)